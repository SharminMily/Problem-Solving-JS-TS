// . Sum of First n Natural Numbers

//  Input: n = 5 → Output: 15 (1+2+3+4+5) = // 15
function sumNatural(n){
  let sum = 0;
  for (let i = 1; i <=n; i++){

    sum +=i ;
  }
  return sum;
  }

